import math
an=float(input('Digite o angulo que voce deseja: '))
sen =math.sin(math.radians(an))
print(f'O angulo de {an}tem SENO de {sen:.2f}')
coss= math.cos(math.radians(an))
print(f'O angulo de {an} tem COSSENO de {coss:.2f}')
tan = math.tan(math.radians(an))
print(f'O aangulo de {an} tem a TANGENTE de {tan:.2f}')
